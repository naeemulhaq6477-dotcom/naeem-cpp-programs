// Use float type to store a fraction number

#include <iostream>   // Input-output library for printing values
using namespace std;  // Standard namespace for using cout directly

int main() {
    float a = 7.5;    // Changed the type of variable from int to float
                      // 'float' can store numbers with decimals, so 7.5 will be stored correctly
    cout << a;        // This will correctly print 7.5 on the screen
    return 0;         // End program successfully
}