#include <iostream>   // This line includes the input-output stream library. 
                      // It allows us to use commands like cout for printing text on the screen.

using namespace std;  // This line lets us use standard C++ commands directly. 
                      // Without it, we would have to write std::cout instead of cout.

int main()            // This is the main function. The program always starts running from here.
{
    int a = 10;       // Declare a variable named 'a' of type integer and assign it the value 10.
                      // 'int' means the variable can only store whole numbers (no decimals).

    cout << a;        // Print the value stored in variable 'a' (which is 10) on the screen.

    return 0;         // End the program and tell the computer everything worked correctly.
}
