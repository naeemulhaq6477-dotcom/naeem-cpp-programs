#include <iostream>
using namespace std;

int main() {
    int i = 0;        // Counter starts at 0
    while (i < 5)     // Loop runs while i is less than 5
    {
        cout << i << "\n";   // Prints the value of i (0, 1, 2, 3, 4)
        i++;                 // Increase i by 1
    }
    return 0;
}